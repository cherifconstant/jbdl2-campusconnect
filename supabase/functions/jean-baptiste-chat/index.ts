import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');
    
    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    const systemPrompt = `Tu es Jean Baptiste, l'assistant virtuel du collège. Tu es sympathique, professionnel et très bien informé sur le collège.

INFORMATIONS SUR LE COLLÈGE:
- Nom: Collège Excellence
- Horaires: 8h-17h du lundi au vendredi
- Inscription: Disponible toute l'année via le formulaire en ligne
- Classes disponibles: De la 6ème à la 3ème
- Services: Restaurant scolaire, bibliothèque, laboratoires, installations sportives
- Contact: Via le formulaire de contact sur le site

TES RESPONSABILITÉS:
- Répondre aux questions sur les inscriptions et les horaires
- Guider les utilisateurs sur le site
- Donner des informations sur les classes et les enseignants
- Expliquer les services du collège
- Orienter vers les bonnes pages et ressources

STYLE DE RÉPONSE:
- Sois concis mais complet
- Utilise un ton amical et professionnel
- Si tu ne sais pas, oriente vers le formulaire de contact
- Propose des actions concrètes (visiter une page, contacter quelqu'un)`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Trop de requêtes, veuillez réessayer dans un moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporairement indisponible." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "Erreur lors de la communication avec l'assistant." }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("chat error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Erreur inconnue" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});